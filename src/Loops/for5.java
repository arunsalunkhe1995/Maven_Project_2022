package Loops;

public class for5
{
	public static void main(String[] args) 
	{
		// print number from 5 to 1 --> 5 4 3 2 1
		// using for loop
		
	 // formula for(starting condition; end condition;  increment/ decrement){}
		
		for(int i=5; i>=1; i--) 
		{
			System.out.print(i+"  "); // use print for horizontal line & " " use for Space between to later 
		}
	}
}

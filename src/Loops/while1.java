package Loops;

public class while1 
{ 
	// print number from 1 to 5 using while loop
	
	// main method body
	public static void main(String[] args) 
	{
	// step1 = starting condition;
		 
		int i=1;
	
	// step2= end condition;
		
		while(i<=5) 
		{
			System.out.println(i);
			
			i++;                         // increment/ decrement condition
		}
	
	}

}
